        <p><h4><b>I. PROFIL LEMBAGA PENGABDIAN MASYARAKAT  </b></h4></p>

        <p class="mtb-15">Kegiatan Program Pengabdian kepada Masyarakat (PPM) merupakan kegiatan penting bagi suatu pendidikan tinggi. Implementasi dan pelaksanaan kegiatan ini dilakukan oleh dosen dan mahasiswa di bawah koordinasi Lembaga Pengabdian kepada Masyarakat. Sebagai salah satu unsur Tri Dharma, kegiatan pengabdian masyarakat mesti dilaksanakan secara terintegrasi dengan unsur Tri Dharma lainnya, yaitu pendidikan dan penelitian. </p>

        <p class="mtb-15">Disamping itu, sebagai universitas yang mendeklarasikan sebagai universitas riset (Research University), maka keterkaitan penelitian dengan pengabdian.harus diprioritaskan. Lebih dari itu, sejalan perkembangan pola pengelolaan perguruan tinggi yang terjadi di Indonesia saat ini, maka kegiatan PPM perlu dipersiapkan dalam rangka pengelolaan Unsri sebagai Badan Layananan Umum (BLU). Keterkaitan pelaksanaan Tri Darma tersebut Pendidikan dan Pengajaran, Penelitian dan Pengembangan dan Pengabdian Kepada Masyarakat </p>

        <p class="mtb-15">Lembaga Pengabdian kepada Masyarakat (LPM) merupakan unsur pelaksana akademik dalam suatu perguruan tinggi untuk melaksanakan salah satu unsur Tri Dharma Perguruan Tinggi, yaitu pengabdian kepada masyarakat. Pada periode awal perkembangan, fungsi LPM masih bergabung dengan Lembaga Penelitian (LP). Sejalan dengan perkembangan yang lebih dinamis dalam kegiatan pengabdian, pada tahun 1980, LPM didirikan sebagai suatu lembaga tersendiri melalui penetapan SK Rektor Nomor: 629/IV/PG/1980, dan Peningkatan Status PPM Unsri menjadi LPM Unsri melalui SK Rektor Nomor : 1902/PT11.1.1/c.6.f/1993. </p>

        <p class="mtb-15">Seperti tercantum dalam SK Rektor Nomor: 629/IV/PG/1980, fungsi dan tugas LPM Unsri adalah :
a) Mengamalkan ilmu pengetahuan, teknologi, dan seni
b) meningkatkan relevansi program pendidikan
c) membantu pemerintah dan masyarakat dalam melaksanakan pembangunan
d) melaksanakan pengabdian kepada masyarakat melalui kerjasama dengan instansi pemerintah, swasta, dan lain-lain. </p>

        <h4><b>II. PROFIL LEMBAGA PENELITIAN </b></h4>

        <p class="mtb-15">Dalam pencapaian visi Universitas Sriwijaya Menjadi Universitas terkemuka dan berbasis riset, unggul di berbagai cabang ilmu, teknologi dan seni pada tahun 2025, Lembaga Penelitian Universitas Sriwijaya mengembangkan program penelitian unggulan dalam bidang Pangan, Energi Baru dan Terbarukan dan Lingkungan dan meningkatkan riset kolaborasi dengan institusi lain baik didalam maupun di luar negeri. </p>

        <p class="mtb-15">Kebijakan universitas dalam memfasilitasi kegiatan penelitian, seminar, publikasi, paten, buku ajar dan riset kolaborasi khususnya di bidang unggulan baik secara nasional maupun internasional telah menghasilkan sejumlah kegiatan penelitian dan riset kolaborasi dengan instansi lain, publikasi nasional dan internasional, sejumlah paten dan buku ajar. Luaran hasil penelitian digunakan untuk meningkatkan ranking Universitas Sriwijaya dan teknologi yang dihasilkan berpotensi diaplikasikan diaplikasikan di masyarakat dan dunia industri. </p>
        <p class="mtb-15">Lembaga Penelitian Universitas Sriwijaya mengelola kegiatan penelitian, diseminasi hasil penelitian melalui seminar dan publikasi pada jurnal nasional terakreditasi dan jurnal internasional, hasil penelitian yang di patenkan dan buku ajar. Kegiatan pengembangan sumber daya manusia (SDM) meliputi pelatihan metodologi penelitian, penulisan proposal penelitian dan jurnal diakan setiap tahunnya. </p>

